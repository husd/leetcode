public class MajorityElement {

    //思路1： 暴力方法
    //思路2： 排序，中间的那个数，一定是
    //思路3： 随机取一个数字，大概率会是
    //思路4： 投票算法
    public int majorityElement(int[] nums) {

        int count = 0;
        Integer candidate = null;

        for (int num : nums) {
            if (count == 0) {
                candidate = num;
            }
            count += (num == candidate) ? 1 : -1;
        }

        return candidate;
    }
}
